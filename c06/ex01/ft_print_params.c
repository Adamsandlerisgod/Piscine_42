#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	main(int ac, char **argv)
{
	int i;
	int j;

	j = 1;
	while (j < ac)
	{
		i = 0;
		while (argv[j][i] != '\0')
		{
			ft_putchar(argv[j][i]);
			i++;
		}
		write(1, "\n", 1);
		j++;
	}
	return (0);
}
